import UIKit

extension UIColor {
    static func appLightBlueColor() -> UIColor {
        return UIColor(
            red: 35.0 / 255.0,
            green: 95.0 / 255.0,
            blue: 190.0 / 255.0,
            alpha: 1.0
        )
    }

    static func appDarkBlueColor() -> UIColor {
        return UIColor(
            red: 3.0 / 255.0,
            green: 33.0 / 255.0,
            blue: 85.0 / 255.0,
            alpha: 1.0
        )
    }
}
